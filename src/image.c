void move_player(struct player *p, int direction) {

	/*erase previous location*/
	draw_one_block(p->row, p->col, BLACK);

	/*update location*/
	switch (direction) {
		case 0: //left
		{
			if (p->col > 0)
				(p->col)--;
			break;
		}
		case 1: //Right
		{
			if (p->col < 29)
				(p->col)++;
			break;
		}
		case 2: //Up
		{
			if (p->row > 0)
				(p->row)--;
			break;
		}
		case 3:
		{//Down
			if (p->row < 19)
				(p->row)++;
			break;
		}
	}
	collision_check(p);
	/*draw new location*/
	draw_player(p);
}

